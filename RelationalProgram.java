package com.onesoft.day1;

public class RelationalProgram {
	public static void main(String[] args) {
		
		int a=5;
		int b=3;
		
		boolean islesser= a<b;
		boolean isgreater =a>b;
		boolean isGreaterThanOrEqual=a>=b;
		boolean isLesserThanOrEqual=a<=b;
		boolean isEqual=a==b;
		boolean isNotEqual=a!=b;
		
		System.out.println(islesser);
		System.out.println(isgreater);
		System.out.println(isGreaterThanOrEqual);
		System.out.println(isLesserThanOrEqual);
		System.out.println(isEqual);
		System.out.println(isNotEqual);
		
	}

}
